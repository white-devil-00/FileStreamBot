@StreamBot.on_message(filters.command(["start","Start"]))
async def start(_, m: Message):
    await m.reply(
        f'__Hi {m.from_user.mention(style="md")}, I\'m File to Link Bot__\n**__Send me a file to get an instant stream link ...__**'
    )

